// Original function copy-pasted from http://www.ecse.rpi.edu/Homepages/wrf/Research/Short_Notes/pnpoly.html
int pnpoly(int nvert, float *vertx, float *verty, float testx, float testy)
{
	int i, j, c = 0;
	for (i = 1, j = i-1; i < nvert; j = i++) {
		if ( ((verty[i]>testy) != (verty[j]>testy)) &&
			(testx < (vertx[j]-vertx[i]) * (testy-verty[i]) / (verty[j]-verty[i]) + vertx[i]) )
			c = !c;
	}
	return c;
}

// Modification for the structure, knowing that the first vertex is repeated in the last position of the array
int pnpoly2(int nvert, float *vertex, float testx, float testy)
{
	int i, j, c = 0;
	for (i = 1, j = i-1; i < nvert; j = i++) {
		if ( ((vertex[3*i+1]>testy) != (vertex[3*j+1]>testy)) &&
			(testx < (vertex[3*j]-vertex[3*i]) * (testy-vertex[3*i+1]) / (vertex[3*j+1]-vertex[3*i+1]) + vertex[3*i]) )
			c = !c;
	}
	return c;
}

// Cuda version
__global__ void pnpolyGPU(const float *vertex, float testx, float testy, int* results)
{
	int id = blockIdx.x;
	int indexOriginX = (blockIdx.x + 1) * 3;
	int indexOriginY = (blockIdx.x + 1) * 3 + 1;
	int indexDestinoX = blockIdx.x * 3;
	int indexDestinoY = blockIdx.x * 3 + 1;

	if ( ((vertex[indexOriginY]>testy) != (vertex[indexDestinoY]>testy)) && (testx < (vertex[indexDestinoX]-vertex[indexOriginX]) * (testy-vertex[indexOriginY]) / (vertex[indexDestinoY]-vertex[indexOriginY]) + vertex[indexOriginX]) )
		results[id] = 1;
	else
		results[id] = 0;
}

